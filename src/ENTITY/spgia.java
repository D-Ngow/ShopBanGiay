package ENTITY;

/**
 *
 * @author LENOVO
 */
public class spgia {
    public String tsp;
    public double gia;

    public spgia() {
    }

    public spgia(String tsp, double gia) {
        this.tsp = tsp;
        this.gia = gia;
    }

    public String getTsp() {
        return tsp;
    }

    public double getGia() {
        return gia;
    }

    public void setTsp(String tsp) {
        this.tsp = tsp;
    }

    public void setGia(double gia) {
        this.gia = gia;
    }
    
}
